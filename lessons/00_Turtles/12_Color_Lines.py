import turtle                           # Tell Python we want to work with the turtle
turtle.setup(width=600, height=600)     # Set the size of the window

tina = turtle.Turtle()  

tina.color("red")
tina.forward(80)

tina.color("orange")
tina.left(90)
tina.forward(80)

tina.color("yellow")
tina.left(90)
tina.forward(80)

tina.color("green")
tina.left(90)
tina.forward(80)



turtle.exitonclick()  



